# A virtual environment is an isolated workspace where you can install packages independently of the system-wide Python. 
# It helps you avoid dependency conflicts between projects.

# Use 
# 1. Project Isolation 
# 2. Version Control 
# 3. Cleaner Global Environment 
# 4. Better Reproducibility 


# Step 1: Create a virtual environment    python -m venv env

# Step 2: Activate the virtual environment    .\env\Scripts\activate

# Step 3: Install packages    pip install requests

# Step 4: Deactivate when done      deactivate

